package model;

/**
 *
 * @author Admin
 */
public class Question {
    private int qid;
    private String content;

    public Question() {
    }

    public Question(int qid, String content) {
        this.qid = qid;
        this.content = content;
    }

    public int getQid() {
        return qid;
    }

    public void setQid(int qid) {
        this.qid = qid;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
    
}
